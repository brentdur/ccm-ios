#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return [data count];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    MultiSelectCell *cell = [tableView dequeueReusableCellWithIdentifier:@"MultiCell" forIndexPath:indexPath];
    
    if (cell == nil){
        cell = [[MultiSelectCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:@"MultiCell"];
    }
    
    [[cell button] setTitle:[data objectAtIndex:indexPath.row] forState:UIControlStateNormal];
    
    return cell;
}

